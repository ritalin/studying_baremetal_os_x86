;********************************************************************************
; グローバルデスクリプタ
;********************************************************************************
GDT:        dq 00_0_0_0_0_000000_0000h  ; NULL
.cs_kernel: dq 00_C_F_9_A_000000_FFFFh  ; CODE(4GB)
.ds_kernel: dq 00_C_F_9_2_000000_FFFFh  ; DATA(4GB) 
.ldt:       dq 00_0_0_8_2_000000_0000h  ; 
.gdt_end:

GDTR:       dw GDT.gdt_end - GDT        ; ディスクリプタデーブルのリミット
            dd GDT                      ; ディスクリプタテーブルのアドレス

;********************************************************************************
; ローカルデスクリプタ
;********************************************************************************
LDT:        dq 00_0_0_0_0_000000_0000h  ; NULL
.cs_taks_00:dq 00_C_F_9_A_000000_FFFFh  ; カーネルタスク用のため領域はGDTと共有
.ds_taks_00:dq 00_C_F_9_A_000000_FFFFh  ; カーネルタスク用のため領域はGDTと共有
.ldt_end:

LDT_LIMIT equ LDT.ldt_end - LDT